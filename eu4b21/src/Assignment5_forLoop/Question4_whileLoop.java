package Assignment5_forLoop;

public class Question4_whileLoop {

	public static void main(String[] args) {
	
		int number = 35;
		int product = 0;
		
		int i = 1;
		while (i<number) {

		  		  product = i*10;
		  		  if (product<100) {
		  			  
		  			  System.out.print(product + " ");
		  			
		  		  }
		  		  
		  		i++;
		}
					
	}

}
