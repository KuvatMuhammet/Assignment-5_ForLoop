package Assignment5_forLoop;

public class Question5_multTableOf12 {

	public static void main(String[] args) {
		
		int num=12;
		
		System.out.println("Multiplication table");
		System.out.println("--------------------");
					
		for (int i=1; i<=10; i++) {
			
			System.out.println(num + "x" + i + "=" + (num*i));
		}
		
	}

}
